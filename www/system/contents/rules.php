<div class="part">
  <div id="camping" class="title">IX. Stanování<span class="toTop rightalign"><a href="#top">&nbsp;&nbsp;&nbsp;</a></span></div>
  
  <div>Součástí areálu koupaliště je plocha k pronajmutí na umístění stanů. Dočasní uživatelé této plochy mohou používat hygienická zázemí koupaliště (WC, teplé sprchy…). Provozovatel bude udržovat pronajímané prostory posečené a uklizené. Osoby kempující v areálu koupaliště vhazují odpadky do odpadkových košů a popelnic. Větší odpadky mohou vhodit přímo do kontejneru na odpadky, který provozovatel nechává pravidelně vyvážet. Osoba kempující se při příjezdu nahlásí v pokladně, uhradí poplatek, zapíše se do knihy ubytovaných, obdrží průchodku a číslo popisné, které umísí na stan pro možnost kontroly provozovatele. Zároveň bude poučena o umístění hygienických zařízeních a budou mu poskytnuty informace potřebné k jeho pobytu. Při odjezdu odevzdá na pokladně průchodku a popisné číslo. – ubytování a stanování je řešeno samostatným provozním řádem. Je zákaz parkování osobními automobily a dodávkami u stanů, na parkování slouží odstavné parkoviště.</div>
  
</div>  

