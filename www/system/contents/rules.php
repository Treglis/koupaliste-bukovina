<div class="part">
  <div class="title">I. Vstup na koupaliště<span class="toTop rightalign"><a href="#top">&nbsp;&nbsp;&nbsp;</a></span></div>
  <div>
    <ul>
      <li>Koupaliště je otevřeno v letní sezoně. Provoz je podmíněn povětrnostními vlivy, teplotou vody a vzduchu.</li>
      <li>Vstup do celého areálu koupaliště je povolen pouze s platnou vstupenkou, kterou vydává pokladna koupaliště.</li>
      <li>Prodej vstupenek začíná otevřením koupaliště a končí jeho uzavřením. Denní vstupenka platí pouze v den jejího vydání. Bez platné vstupenky bude návštěvník vykázán z areálu koupaliště.</li>
      <li>Zakoupením vstupenky a vstupem na koupaliště se každý návštěvník dobrovolně podrobuje tomuto provoznímu řádu a pokynům odpovědných pracovníků koupaliště.</li>
      <li>Dětem mladším 10 let je návštěva celého areálu koupaliště povolena pouze v doprovodu osoby starší 18 let. (za děti mladších 15-ti let používající veškeré herní vybavení ručí rodiče-doprovod)</li>
      <li>Při plném obsazení koupaliště je může provozovatel uzavřít a přerušit prodej vstupenek, než se koupaliště uvolní.</li>
    </ul>
  </div>
</div>

<div class="part">
  <div class="title">II. Provozní pokyny pro návštěvníky<span class="toTop rightalign"><a href="#top">&nbsp;&nbsp;&nbsp;</a></span></div>
  <div>
    <ul>
      <li>Za ztrátu peněz a cenných předmětů provozovatel neručí. Předměty nalezené v prostorách koupaliště odevzdá nálezce plavčíkovi, popřípadě v pokladně.</li>
      <li>Návštěvníci jsou povinni užívat čistý a slušný koupací oděv a dodržovat zásady hygieny.</li>
      <li>V zájmu vlastní bezpečnosti jsou neplavci nebo méně zdatní plavci povinni koupat se jen v prostorách vyhrazených pro neplavce a dbát pokynů plavčíka.</li>
      <li>Návštěvníci jsou povinni šetřit zařízení koupaliště, zbytečně neplýtvat vodou. Jsou povinni hradit škody nebo ztráty, které byly jejich vinou způsobeny na zařízení koupaliště nebo majetku ostatních návštěvníků.</li>
      <li>Návštěvníci, zejména osoby starší nebo postižené tělesnou vadou, se musí ve vlastním zájmu pohybovat opatrně na hladkých a mokrých podlahách a na nerovném terénu a vyvarovat se tak uklouznutí a upadnutí, popř. úrazu. Provozovatel koupaliště nenese odpovědnost za škody, poranění a úrazy, které si způsobili návštěvníci sami svojí neopatrností nebo nedodržením tohoto řádu.</li>
      <li>Návštěvníci jsou povinni dbát o vlastní bezpečnost a bezpečnost ostatních koupajících.</li>
      <li>Návštěvníci musí dodržovat stanovenou provozní dobu, zůstávat na koupališti a používat jeho zařízení mimo tuto dobu není dovoleno.</li>
      <li>S přáním a stížnostmi týkajícími se provozu nebo zaměstnanců koupaliště se návštěvníci obrací na vedoucího koupaliště nebo na vedení provozovatele.</li>
      <li>Majitelé psů jsou plně odpovědni za chování psů a veškeré způsobené škody a újmy na zdraví návštěvníků koupaliště hradí na své náklady.</li>
      <li>Vstupovat na koupaliště s jízdními koly je povoleno, za ztrátu ev. případné poškození provozovatel neručí.</li>
    </ul>
  </div>
</div>

<div class="part">
  <div class="title">III. Na koupališti je zakázáno<span class="toTop rightalign"><a href="#top">&nbsp;&nbsp;&nbsp;</a></span></div>
  <div>
    <ul>
      <li>Chovat se způsobem, který ohrožuje osobní bezpečnost a pořádek, nebo hlukem rušit klid ostatních.</li>
      <li>Volat o pomoc bez vážné příčiny.</li>
      <li>Vzájemně se potápět, srážet a vhazovat druhé osoby do vody, pobíhat po ochozu bazénu (vzhledem k nebezpečí úrazu) a skákat do vody mimo místa vyhrazená.</li>
      <li>Vstupovat do prostor návštěvníkům nepřístupných, označených výstražnými tabulkami a páskami.</li>
      <li>Svévolně znečišťovat vodu i ostatní prostory koupaliště (praní prádla apod.).</li>
      <li>Kouřit ve všech budovách, uzavřených prostorách, vcházet do oddělení nebo míst určených pro osoby druhého pohlaví, v prostorách koupaliště odkládat skleněné a jiné předměty ohrožující bezpečnost návštěvníků.</li>
      <li>Zdržovat se v kabinách mimo nezbytně nutnou dobu k převléknutí.</li>
      <li>Hrát s tvrdými míči kopanou, házenou apod. mimo místa k tomu vyhrazená.</li>
      <li>Neoprávněně používat záchranných předmětů první pomoci.</li>
      <li>Používat v koupališti netypizovaných plovacích kol.</li>
      <li>Na koupaliště není dovolen přístup osobám postiženým horečkou, zánětem očních spojivek, nakažlivými nebo odpor budícími chorobami, kožními parazity nebo vyrážkami, chorobami provázenými výtokem např. hnisu apod., dále bacilonosičům střevních chorob, osobám nacházejícím se v karanténě pro výskyt nakažlivé nemoci v kolektivu, členům rodin nebo příslušníkům domácností, v nichž se vyskytla nakažlivá choroba a kde nemocný není od ostatních osob izolován, osobám zahmyzeným a opilým.</li>
    </ul>
  </div>
</div>

<div class="part">
  <div class="title">IV. Vyloučení z návštěvy<span class="toTop rightalign"><a href="#top">&nbsp;&nbsp;&nbsp;</a></span></div>
  <div>
    <ul>
      <li>Z koupaliště bude bez nároku na vrácení vstupného vykázán každý, kdo přes napomenutí přestoupí ustanovení tohoto provozního řádu nebo neuposlechne pokynů odpovědného pracovníka (vedoucího koupaliště, plavčíka apod.), vstoupí do prostor návštěvníkům nepřístupným, opije se nebo se chová jiným nepřístojným způsobem. Neopustí-li v takových případech návštěvník koupaliště na vyzvání, je pracovník koupaliště povinen rušitele vyvést, popřípadě požádat o pomoc Policii.</li>
    </ul>
  </div>
