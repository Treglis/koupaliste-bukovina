<div class="part">
  <div class="title">IV. Kontrola jakosti vody v bazénu<span class="toTop rightalign"><a href="#top">&nbsp;&nbsp;&nbsp;</a></span></div>
  
  <div class="textspaced">Vzorky odebírá akreditovaná nebo autorizovaný laboratoř, výsledky rozboru vody jsou prostřednictvím provádějící laboratoře předávány příslušné Krajské hygienické stanici v předepsané elektronické podobě. Archivovány jsou po dobu 5ti let. K dispozici u provozovatele.</div>
  <div class="textspaced">Voda v bazénu je v minimální míře upravována chlorováním (prováděno denně po denním provozu) za použití chlornanu sodného. Při manipulaci s ním musí personál používat osobní ochranné pomůcky – pracovní oděv, brýle. Manipulace a skladování chlornanu sodného se řídí bezpečnostním listem výrobku. Desinfekční prostředky uloženy na pokladně. Ráno před zahájením provozu jsou měřeny a do provozního deníku zapisovány hodnoty celkového chloru, které nesmí překročit 0,3 mg/l (při vyšší hranici není možné provoz zahájit), ovšem obvykle je hodnota celkového chloru na hranici nebo pod hranicí detekce 0,1 mg/l, dále jsou ve stejné frekvenci měřeny a zapisovány hodnoty pH. Měření hodnoty celkového chloru je rovněž zajišťováno laboratorně při odběru vzorků rekreační vody provádějící laboratoří.</div>
  <div class="textspaced">Četnost a rozsah kontroly napouštěcí a rekreační vody je zajištěna dle požadavků vyhlášky č. 238/2011 Sb..</div>
  <div class="textspaced">Kontrola je zahájena před předpokládaným počátkem, tak aby byl v době plánovaného otevření znám výsledek kontroly.</div>
  <div class="textspaced">V případě, že se sníží průhlednost nebo dojde k výskytu sinic, postupuje se hodnocením vodního květu dle přílohy č. 2 k vyhlášce. Snížení vzorkování je možné ve smyslu § 15 odst. 4 vyhlášky č. 238/2011 Sb. bude dopředu konzultováno s KHS.</div>
  <div>V případě překročení limitní hodnoty mikrobiologického ukazatel, bude bez prodlení zajištěn opakovaný odběr. Pokud tento opakovaný odběr potvrdí nevyhovující nález, nebude možné koupaliště provozovat – a to minimálně do odstranění závady, doložitelné vyhovujícím rozborem vody. Při nedodržení hygienického limitu u jiného než mikrobiálního ukazatele, bude koupaliště označeno tak, aby o této skutečnosti informován každý návštěvník.</div>
  
</div>

