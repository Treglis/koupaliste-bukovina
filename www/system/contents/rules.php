<div class="part">
  <div class="title">II.  Čištění bazénu, úprava vody a úklid areálu koupaliště<span class="toTop rightalign"><a href="#top">&nbsp;&nbsp;&nbsp;</a></span></div>
  
  <div class="subtitle">1. Čištění bazénu</div>
  <div class="textspaced">Mechanické čištění se dle potřeby se provádí koštětem, před provozem a během provozu se z vody odstraňují nečistoty sítem na násadě.</div>
  <div>Bazén má stálý přítok ředící vody.</div>
  
  <div class="subtitle">2. Úklid odpadků</div>
  <div>Odpadkové koše jsou rozmístěny po celém areálu koupaliště. Vyprazdňují se dle potřeby, nejméně však jednou denně před začátkem provozu. Odvoz odpadků zajišťuje provozovatel koupaliště.</div>
  
  <div class="subtitle">3. Úklid areálu koupaliště</div>
  <div>Každý den se před zahájením provozu provede úklid všech prostor, včetně ploch určených pro odpočinek a slunění. Nejméně jednou denně se čistí podlahy šaten, umýváren a záchodů včetně sedátka, které se omývá k tomu určeným desinfekčním prostředkem a během dne dle potřeby. Hygienické prostředky se používají dle návodu výrobce ve virucidní, baktericidní a fungicidní koncentraci.</div>
</div>

